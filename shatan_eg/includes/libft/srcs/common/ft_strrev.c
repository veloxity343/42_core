#include "libft.h"

char	*ft_strrev(char *s)
{
	char	*p2;
	char	tmp;

	p2 = s + ft_strlen(s) - 1;
	while (s < p2)
	{
		tmp = *s;
		*s++ = *p2;
		*p2-- = tmp;
	}
	return (s);
}
